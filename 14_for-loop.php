<?php

// Initialize a variable with the value 0
$a = 0;

// Start a for loop with the following parameters:
// 1. Initialization: $i = 0
// 2. Condition: Continue looping while $i < 10
// 3. Increment: $i++ (increase $i by 1 after each iteration)

for ($i = 0; $i < 10; $i++) {
    // Output the current value of $a
    echo $a;
    
    // Increment $a by 1 in each iteration
    $a++;
}
?>
