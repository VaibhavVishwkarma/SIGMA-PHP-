<?php
// Initialize a variable with the value 1
$a = 1;

// Start a do-while loop
// The code inside the loop will execute at least once before checking the condition
do {
    // Output the string "Vaibhav" followed by a line break
    echo "Vaibhav <br>";
    
    // Increment $a by 1 in each iteration
    $a++;
} while ($a < 10); // Continue looping as long as $a is less than 10

