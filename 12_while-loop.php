<?php

// Initialize a variable with the value 0
$a = 0;

// Start a while loop that continues as long as $a is less than 10
while ($a < 10) {
    // Increment $a by 1 in each iteration
    $a++;
    
    // Output a line break for better readability
    echo "<br>";
    
    // Output the current value of $a
    echo $a;
}
